package day03_Variable;

public class EscapeSequences {
/*
 \n( or \r): starts from a new line
 \t: horizontal tab is created before the next line.
\\: prints out a single backslash on the console.
\': prints a single quotation mark.
\": prints double quotation marks.

 */
	public static void main(String[] args) {
		System.out.println("Hello Cybertek\n Batch12 Students");
		System.out.print("Tomorrow if off\n");
		System.out.print("True\n");
		System.out.print("False\n");
		System.out.println("\t class starts tomorrow");
		System.out.println("\\muhtar");
		System.out.println("\'Java");
		System.out.println(" the Book is called \"Game of Thrones\"");
		
	}
}

