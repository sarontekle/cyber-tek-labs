package day21_whileLoops;

public class Do_While {
	public static void main(String[] args) {
		boolean A=false;
		while (A) {
			System.out.println("Hello");
		}
		/*
		 do-while: do{
		 statement;
		 }
		 while(condition);
		 do block gets executed first before checking for the condition, as long as the condition is true, it keeps executeing.
		 */
	}

}
